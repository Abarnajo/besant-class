# string slicing
a = " World Class"
print(a[1:3])

#Upper
a = "world class"
print(a.upper())

#lower
a = "WORLD CLASS"
print(a.lower())

#strip
a = "World, class"
print(a.strip())

#Replace
a = "world class"
print(a.replace("w", "G"))

#split
a = "world, class"
print(a.split(","))






